<p><strong>Working with Menus</strong></p>
<p>This admin page contains the list of all currently setup menus. Each menu has categories and each category has items.</p>
<ul>
  <li>Create a new menu and give it a title and description.</li>
  <li>Add Categories to the new menu.</li>
  <li>Add items to the new menu and select a category for each.</li>
</ul>

<p><strong>Creating a new menu</strong></p>

<p>The plugin comes with a menu already set up so you can see how to work with menus, categories and items. (Because the free version of the plugin allows only one menu, free version users will need to edit this menu.) To start a new menu, simply click the "Create a menu" button in the main "Manage Menus" area and fill out a name and, if desired, a description. From there, you can edit/add categories and items.</p>
